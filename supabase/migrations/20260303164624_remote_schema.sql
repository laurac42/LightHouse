CREATE OR REPLACE FUNCTION public.update_buyer_profile(p_id uuid, p_budget numeric, p_family_size smallint, p_preferred_num_bedrooms smallint, p_preferred_property_types text[], p_preferred_locations text[])
 RETURNS void
 LANGUAGE plpgsql
AS $function$
begin
  update buyer_profiles
  set 
  family_size = p_family_size,
  budget = p_budget,
  preferred_num_bedrooms = p_preferred_num_bedrooms,
