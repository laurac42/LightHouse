CREATE OR REPLACE FUNCTION public.get_users_granted_by_agent()
 RETURNS TABLE(id uuid, first_name text, last_name text, email text)
 LANGUAGE plpgsql
AS $function$ 
begin
return query
  select distinct u.id, u.first_name, u.last_name, u.email
  from users u
  inner join user_roles ur on u.id = ur.user_id
  where ur.granted_by = auth.uid();
end;
$function$
;

CREATE OR REPLACE FUNCTION public.handle_new_user()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO ''
AS $function$
begin
  insert into public.users (id, email)
  values (new.id, new.email);

  if new.raw_user_meta_data ? 'role' then
  insert into public.user_roles(user_id, role, granted_by)
  values (new.id, new.raw_user_meta_data->>'role', (new.raw_user_meta_data->>'granted_by')::uuid);

  if new.raw_user_meta_data->>'role' = 'agent' then
  insert into public.estate_agent_profiles(id, estate_agency_id)
  values (new.id, (new.raw_user_meta_data->>'agency_id')::uuid);
  end if;
  end if;
  return new;
end;
$function$
;

